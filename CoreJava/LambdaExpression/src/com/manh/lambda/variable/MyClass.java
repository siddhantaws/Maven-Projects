package com.manh.lambda.variable;

public class MyClass {
	
	
	
	// Existing static method with a single argument
	public static void existingStaticMethod(String name) {
		System.out.println("Your name is " + name);
	}

	// Existing static method with two arguments
		public static void existingStaticMethod(String name, int age) {
			System.out.println(name + " is " + age + " years old.");
		}
}
